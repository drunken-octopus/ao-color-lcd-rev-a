// GRAPHIC ITEM STRUCTURES

struct Button_Struct {
     int x_pos;
     int y_pos;
     int x_size;
     int y_size;
     unsigned char color_r;
     unsigned char color_g;
     unsigned char color_b;
     int font;
     unsigned char widget;
     unsigned int options;
     char text[40];
};

struct Text_Struct {
     int x_pos;
     int y_pos;
     int font;
     unsigned int options;
     char text[20];
};

struct Clock_Struct {
     int x_pos;
     int y_pos;
     int radius;
     unsigned int options;
     unsigned int hours;
     unsigned int minutes;
     unsigned int seconds;
     unsigned int milliseconds;
};

struct Progress_Struct {
     int x_pos;
     int y_pos;
     int width;
     int height;
     unsigned int options;
     unsigned int value;
     unsigned int range;
};

struct Slider_Struct {
     int x_pos;
     int y_pos;
     int width;
     int height;
     unsigned int options;
     unsigned int value;
     unsigned int range;
};


